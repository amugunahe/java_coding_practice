package com.operex; // 야, 이 코드가 여기 산다. 주소 박아놨으니까 딴 데서 헤매지 마라.

/* 삼항 조건 연산자 -> 삼항연산자 // 조건에 따라 딱 둘 중 하나 고르는 연산자다.
조건항 ? 항1(true) : 항2(false) // 조건이 참이면 항1, 거짓이면 항2 고른다.
*/ 

public class OperEx05 { // 야, OperEx05 이름으로 프로그램 시작한다. 이 파일 이름이랑 똑같으니까 틀리면 뒤진다.

	public static void main(String[] args) { // 여기가 진짜 시작점이다. 무조건 여기부터 실행된다고 보면 됨.

		int a = 20, b = 30, max; // a에 20, b에 30 박아 넣고, max라는 빈 공간도 하나 만들었다.

		// 이 줄이 삼항 연산자의 핵심이다!
		// max = a > b ? ++a : ++b;
		// 1. 조건 확인: 'a > b' (20 > 30) 이거 맞냐?
		//    - 20이 30보다 클 리가 없지? 그러니까 '거짓(false)'이다.
		// 2. 결과 고르기: 조건이 '거짓'이니까 콜론(:) 뒤에 있는 '++b'를 실행한다.
		//    - '++b'는 b 값을 먼저 1 올리고 나서 그 값을 쓰는 거다.
		//    - 그래서 b는 30에서 31이 되고, 이 31이 max에 박힌다.
		max = a > b ? ++a : ++b;

		System.out.println("max: " + max); // max에 뭐 들어있는지 화면에 보여준다.
		System.out.println("a: " + a); 	 // a에 뭐 들어있는지 화면에 보여준다.
		System.out.println("b: " + b); 	 // b에 뭐 들어있는지 화면에 보여준다.
	}
}
