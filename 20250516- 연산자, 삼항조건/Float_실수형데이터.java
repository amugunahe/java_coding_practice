package com.test;


//float: 실수형 데이터(4byte)
//실수형의 기본 자료형은 double

public class Float {

	public static void main(String[] args) {
		float var1;
		var1=3.4f;
		double var2;
		var2=55.55;
		
		System.out.println("var1: "+ var1);
		System.out.println("var2: "+ var2);
	
	int a= 99;
	System.out.printf("%b", (a>=100)&& (a<=200));
	}
	
	

}
