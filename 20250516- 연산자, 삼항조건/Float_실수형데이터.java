package com.test; // 야, 이 코드가 여기 산다. 주소 박아놨으니까 딴 데서 헤매지 마라.

/* float: 실수형 데이터(4byte)
실수형의 기본 자료형은 double
*/ // float는 소수점 숫자 쪼잔하게 4바이트 쓰는 새끼고, 자바에서 소수점은 원래 double이 대장이다.

// 빡대가리도 이해하는 핵심 정리
// * float vs double:
//   - float: 소수점 숫자 저장하는데, 뒤에 꼭 'f' 붙여줘야 함. 안 그러면 에러 쳐맞는다.
//   - double: 얘도 소수점 숫자 저장하는데, 'f' 안 붙여도 됨. 이게 기본이라서 그럼. 보통 소수점 쓸 때는 이거 쓴다.
// * public class Float: 여기 나오는 Float는 파일 이름이랑 똑같이 써야 함. 안 그러면 컴파일 안 돼서 빡친다.
// * public static void main(String[] args): 여기가 진짜 시작점이다. 무조건 여기부터 실행된다고 보면 됨.
// * System.out.println(...): 화면에 뭐 보여주는 명령어다.
// * System.out.printf("%b", ...): 형식 맞춰서 화면에 보여주는 건데, %b는 참(true) 아니면 거짓(false) 보여줄 때 쓴다.
// * && (논리곱, AND): '그리고' 이거임. 양쪽 조건 다 맞아야 참(true)이다. 하나라도 틀리면 무조건 거짓(false).


public class Float { // 야, Float 이름으로 프로그램 시작한다. 이 파일 이름이랑 똑같으니까 틀리면 뒤진다.

	public static void main(String[] args) { // 여기가 진짜 시작점이다. 무조건 여기부터 실행된다고 보면 됨.

		float var1; // var1이란 놈한테 float 자리 미리 깔아줬다. 이제 여기 소수점 숫자 넣을 수 있음.
		var1 = 3.4f; // var1에 3.4 박아 넣었다. float이라고 'f' 안 붙이면 에러 쳐맞으니까 조심해라.

		double var2; // var2란 놈한테 double 자리 깔아줬다. 얘도 소수점 숫자 넣는다.
		var2 = 55.55; // var2에 55.55 넣었다. double은 대장이라 뒤에 뭐 안 붙여도 됨. 개꿀이지?

		System.out.println("var1: " + var1); // var1에 뭐 들어있는지 화면에 보여준다. "var1: " 이거랑 같이 나옴.
		System.out.println("var2: " + var2); // var2에 뭐 들어있는지 화면에 보여준다. "var2: " 이거랑 같이 나옴.

		int a = 99; // a라는 정수(int) 변수에 99 넣었다.

		System.out.printf("%b", (a >= 100) && (a <= 200));
		// 이거는 형식 맞춰서 화면에 보여주는 코드임. %b는 참(true) 아니면 거짓(false) 보여줄 때 쓴다.
		// 괄호 안에 (a >= 100) && (a <= 200) 이 조건식인데
		//   - (a >= 100): a가 100보다 크거나 같냐? (a가 99니까 거짓)
		//   - (a <= 200): a가 200보다 작거나 같냐? (a가 99니까 참)
		//   - &&: '그리고' 이거임. 양쪽 다 참이어야 진짜 참이다.
		// 결론은 (거짓 && 참)이니까 최종 결과는 '거짓'이 나온다. 이걸 화면에 뿌려줌.
	}
}
