/* 산술 연산자 = > +, -, *, /, % // 더하고 빼고 곱하고 나누고 나머지 구하는 새끼들임.
byte, short, char, int 사이의 연산 결과는 int형 // 4바이트 미만 정수형끼리 계산하면 무조건 int 됨.
long, float, double 사이의 연산 결과는 큰 자료형으로 결정됨 // 큰 놈끼리 계산하면 더 큰 놈이 승리함.
*/ // 이 코드 설명이 여기 박혀있다.

public class OperEx02 { // 야, OperEx02 이름으로 프로그램 시작한다. 이 파일 이름이랑 똑같으니까 틀리면 뒤진다.

	public static void main(String[] args) { // 여기가 진짜 시작점이다. 무조건 여기부터 실행된다고 보면 됨.

	short a, b; // a랑 b한테 short 자리 깔아줬다. 2바이트 정수형임.

	a = 10; // a에 10 박아 넣었다.
	b = 10; // b에 10 박아 넣었다.
	// a = b = 10; // 이렇게 한 방에 넣어도 되는데, 보통 따로 쓰는 게 보기 편함.

	int c = a + b; // 이 줄이 자동 형 변환 개쩌는 부분이다.
	// a랑 b 둘 다 short인데, 둘이 더하면 갑자기 int로 변신함. (short끼리 더했다고 short가 되는 게 아님)
	// 그래서 이 결과를 int 타입인 c에 넣어야 한다.

	System.out.println("c의 값은: " + c); // c에 뭐 들어있는지 화면에 보여준다. "c의 값은: " 이거랑 같이 나옴.
	}
}
