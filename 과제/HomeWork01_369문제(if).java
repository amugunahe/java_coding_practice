package com.test; // 이 코드 덩어리가 속한 폴더(패키지) 이름이다.

/*
  HomeWork01 클래스: **존나 유명한 3, 6, 9 게임 박수세기 프로그램이다!**
 
  핵심:
  - **목표:** 사용자한테 1부터 99 사이의 정수를 입력받아서, 그 숫자에 3, 6, 9가 몇 개 들어있는지에 따라 박수를 출력한다.
  - **규칙:**
  - 3, 6, 9가 한 개 있으면: "박수 짝"
  - 3, 6, 9가 두 개 있으면: "박수 짝짝"
  - **주요 로직:**
  1. 사용자 입력 받기.
  2. **입력 값 유효성 검사:** 1~99 범위 벗어나면 프로그램 끝낸다! (존나 중요!)
  3. **자릿수 분리:** 입력받은 숫자를 '십의 자리'와 '일의 자리'로 쪼갠다. (이게 핵심!)
  - 십의 자리: `num / 10` (예: 36 -> 3, 7 -> 0)
  - 일의 자리: `num % 10` (예: 36 -> 6, 7 -> 7)
  4. 각 자리에 3, 6, 9가 있는지 확인하고 박수 카운트를 올린다.
  5. 박수 카운트에 따라 최종 박수를 출력한다.
 */

import java.util.Scanner; // 사용자 입력받는 도구 'Scanner' 가져오기. (키보드 입력 받을 때 존나 필수다!)

public class HomeWork01 { // 'HomeWork01'이라는 이름의 공개(public) 클래스를 선언한다.

	public static void main(String[] args) { // ★★★ 여.기.서.부.터. 프로그램 시작점이다! 무조건 여기부터 봄. ★★★
		// 'main' 메소드 안에 있는 코드들이 순서대로 실행될 거다
		// 'public static void'는 자바 프로그램의 시작 메소드임을 나타내는 표준 선언이다.
		// 'String[] args'는 외부에서 뭐 받아올 때 쓰는 건데, 여기선 안 씀.

		Scanner sc = new Scanner(System.in); // 'Scanner' 객체를 만들어서 키보드 입력받을 준비 완료!

		int num = 0;        // 사용자 입력 숫자를 저장할 변수. 초기값은 0.
		int clapCount = 0;  // 3, 6, 9 숫자의 개수를 셀 카운터 변수. 초기값은 0.

		System.out.print("1~99 사이의 정수 입력: "); // 사용자한테 입력하라고 화면에 띄우기. (줄 바꿈 없음)
		num = sc.nextInt(); // 사용자가 키보드로 입력한 정수 값을 'num' 변수에 저장한다.

		// --- ★★★ 존나 중요! 입력 값 유효성 검사 (범위 벗어났는지 확인) ★★★ ---
		// 만약 'num'이 1보다 작거나 (음수, 0) 99보다 크면 (100 이상)
		if (num < 1 || num > 99) { 
			System.out.println("숫자의 범위를 벗어났습니다. 1~99 사이의 정수를 다시 입력하세요."); // 에러 메시지 뱉어라!
			sc.close(); // 입력 도구(Scanner) 사용이 끝났으니 깔끔하게 닫아준다. (자원 낭비 방지)
			return;     // ★★★ 존나 중요! 'return'은 메소드를 즉시 종료시킨다! ★★★
			            // main 메소드에서 return 하면 프로그램이 강제 종료된다.
		}

		// --- ★★★ 존나 중요! 숫자의 각 자릿수 분리 및 3, 6, 9 확인 ★★★ ---
		int tensDigit = num / 10;   // 십의 자리 숫자 계산!
		                            // 예: num이 36이면 36 / 10 = 3 (정수 나눗셈이라 소수점 버림)
		                            // 예: num이 7이면 7 / 10 = 0
		int unitsDigit = num % 10; // 일의 자리 숫자 계산!
		                            // 예: num이 36이면 36 % 10 = 6 (10으로 나눈 나머지)
		                            // 예: num이 7이면 7 % 10 = 7

		// 십의 자리에 3, 6, 9가 있는지 확인.
		// 'tensDigit != 0' 조건은 한 자리 숫자(1~9)일 때 십의 자리가 0이므로, 0을 3,6,9로 세지 않기 위함이다.
		if (tensDigit != 0 && (tensDigit == 3 || tensDigit == 6 || tensDigit == 9)) {
			clapCount++; // 십의 자리에 3, 6, 9 중 하나라도 있으면 'clapCount'를 1 증가시킨다.
		}

		// 일의 자리에 3, 6, 9가 있는지 확인.
		// 'unitsDigit != 0' 조건은 '30', '60', '90' 같은 숫자에서 일의 자리가 0이므로 0을 3,6,9로 세지 않기 위함이다.
		if (unitsDigit != 0 && (unitsDigit == 3 || unitsDigit == 6 || unitsDigit == 9)) {
			clapCount++; // 일의 자리에 3, 6, 9 중 하나라도 있으면 'clapCount'를 1 증가시킨다.
		}

		// --- 최종 결과 출력 ---
		if (clapCount == 2) {       // 'clapCount'가 2개면 (예: 33, 36, 69)
			System.out.println("박수 짝짝"); // "박수 짝짝" 출력.
		} else if (clapCount == 1) { // 'clapCount'가 1개면 (예: 3, 16, 92)
			System.out.println("박수 짝"); // "박수 짝" 출력.
		} else {                    // 'clapCount'가 0개면 (예: 1, 12, 45)
			System.out.println("박수 없음"); // "박수 없음" 출력.
		}

		sc.close(); // 다 썼으면 입력 도구 'Scanner'를 깔끔하게 닫아라.
	}
}
