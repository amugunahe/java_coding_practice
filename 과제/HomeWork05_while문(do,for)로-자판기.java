package com.test; // 이 코드가 속한 패키지(폴더) 이름이다.

/*
  HomeWork05 클래스: **콘솔 기반의 존나 간단한 자판기 프로그램이다!**
 
  핵심:
  - **문제:** 콜라, 사이다, 마운틴, 초코송이, 에이스, 웨하스 메뉴를 보여주고,
  사용자가 메뉴 번호를 입력하면 해당 메뉴를 출력한다.
  0번을 누르면 프로그램이 존나게 종료된다!
  - **`Scanner` 클래스:** 사용자한테 키보드로 입력받을 때 쓰는 존나 유용한 놈이다!
  - **`while(true)`:** 0을 입력하기 전까지 메뉴를 무한히 보여주고 입력을 기다린다!
  - **`if (m == 0)` + `break;`:** 프로그램을 종료하는 존나 중요한 조건이다!
  - **`switch` 문:** 입력된 메뉴 번호(`m`)에 따라 알맞은 메뉴를 존나게 똑똑하게 출력한다!
 */

import java.util.*; // 사용자로부터 키보드 입력을 받기 위해 'Scanner' 클래스가 포함된 java.util 패키지 전체를 불러온다.

public class HomeWork05 { // 'HomeWork05'라는 이름의 공개(public) 클래스를 정의한다.

	public static void main(String[] args) { // ★★★ 프로그램의 시작점! ★★★
		// 이 `main` 메소드 안의 코드가 프로그램이 실행될 때 가장 먼저 동작한다.

		// 1. 사용자 입력을 받기 위한 Scanner 객체 생성
		// `System.in`은 표준 입력 장치(키보드)를 의미한다.
		Scanner sc = new Scanner(System.in); 

		// 2. 사용자 메뉴 선택을 저장할 변수 초기화
		int m = 0; // 사용자에게서 입력받을 메뉴 번호를 저장할 'm' 변수를 선언하고 0으로 초기화한다.

		// 3. 자판기 프로그램 핵심 루프 (while문): 사용자가 0번 누르기 전까지 존나게 돈다!
		// `while (true)`: 이 루프는 조건이 항상 참(true)이므로, 무한히 반복된다.
		// 사용자가 '0'을 입력하여 `break` 문을 만나기 전까지 계속 실행된다.
		while (true) {
			// 사용자에게 메뉴 선택지를 출력한다.
			System.out.print("메뉴 선택하세요=> 콜라-1, 사이다-2, 마운틴-3, 초코송이-4, 에이스-5, 웨하스-6(0 종료):");
			m = sc.nextInt(); // 사용자가 키보드로 입력한 정수(숫자)를 읽어서 'm' 변수에 저장한다.

			// 4. 프로그램 종료 조건 확인 (존나 중요!)
			// 사용자가 '0'을 입력했는지 확인한다.
			if (m == 0) {
				System.out.println("프로그램을 종료한다, 씨발!"); // 종료 메시지를 출력한다.
				break; // `break` 문은 현재 `while` 루프를 즉시 종료하고 루프 다음 코드로 넘어간다.
				         // 이 경우, `main` 메소드의 끝으로 가서 프로그램이 종료된다.
			}

			// 5. 선택된 메뉴에 따라 다른 동작 수행 (switch문): 존나게 똑똑하게 메뉴를 고른다!
			// `switch` 문은 'm' 변수의 값에 따라 여러 경우 중 하나를 선택하여 실행할 때 사용한다.
			switch (m) {
			case (1): // 'm'이 1과 같다면 이 코드를 실행한다.
				System.out.println("콜라 선택!"); // "콜라"를 출력한다.
				break; // 해당 case 실행 후 `switch` 문을 종료한다. `break`가 없으면 다음 `case` 문도 이어서 실행될 수 있다.

			case (2): // 'm'이 2와 같다면 이 코드를 실행한다.
				System.out.println("사이다 선택!");
				break;

			case (3): // 'm'이 3과 같다면 이 코드를 실행한다.
				System.out.println("마운틴 선택!");
				break;

			case (4): // 'm'이 4와 같다면 이 코드를 실행한다.
				System.out.println("초코송이 선택!");
				break;

			case (5): // 'm'이 5와 같다면 이 코드를 실행한다.
				System.out.println("에이스 선택!");
				break;

			case (6): // 'm'이 6과 같다면 이 코드를 실행한다.
				System.out.println("웨하스 선택!");
				break;

			default: // 위의 어떤 `case`에도 해당하지 않을 경우 (즉, 1~6, 0 외의 다른 숫자를 입력했을 때)
				System.out.println("선택하신 상품이 없다, 씨발! 다시 해라!"); // "선택하신 상품이 없습니다." 메시지를 출력한다.
				// 여기서는 `break;`가 생략되어 있지만, `default`가 `switch` 문의 마지막이므로 없어도 무방하다.
			}
		} // `while` 루프 끝

		// 6. 자원 해제 (존나 중요!)
		// 프로그램이 종료될 때, 더 이상 `Scanner` 객체를 사용하지 않으므로,
		// 열려있던 자원(메모리 등)을 시스템에 반환한다. 이는 존나 좋은 프로그래밍 습관이다.
		sc.close();
	}
}
