package com.test;

/*
  [문제 3] N단씩 묶어 출력하는 구구단
  - 목표: 사용자가 입력한 숫자 'n' 만큼 단을 묶어서 출력한다.
  - 예시: n이 3이면 1~3단, 4~6단, 7~9단 이렇게 묶어서 나옴.
  - ★★★ 존나 중요! 9단까지만 출력한다. ★★★
 */

import java.util.Scanner; // 사용자 입력받는 도구 'Scanner' 가져오기.

public class HomeWork03 {

	public static void main(String[] args) { // ★★★ 여.기.서.부.터. 프로그램 시작! ★★★

		Scanner sc = new Scanner(System.in); // 키보드 입력받을 준비!

		System.out.print("단 입력: "); // 사용자한테 '몇 단씩 묶을지' 물어보기.
		int n = sc.nextInt(); // 사용자가 입력한 숫자 'n'에 저장. (예: 3을 입력하면 3단씩 묶음)

		// --- ★★★ 존나 중요! 3중 for문으로 구구단 출력 로직 ★★★ ---
		// 첫 번째 for문 (i): 구구단 '그룹'의 시작 단을 정한다. n개씩 건너뛰며 반복.
		//     (예: n=3이면 i가 1 -> 4 -> 7 순으로 시작 단을 바꾼다)
		for (int i = 1; i < 9; i += n) {
			// 두 번째 for문 (j): 각 단에 곱해지는 숫자 (1부터 9까지. 구구단의 세로 줄).
			for (int j = 1; j <= 9; j++) {
				// 세 번째 for문 (k): 한 줄에 'n'개의 단을 나란히 출력하기 위해 반복.
				//     (예: 1, 2, 3단을 한 줄에 같이 보여주려고)
				for (int k = 0; k < n; k++) {
					// ★★★ 존나 중요! 현재 계산하려는 단(i+k)이 9단 이하일 때만 출력! ★★★
					if ((i + k) <= 9) {
						// '%d * %d = %d\t\t' 형태로 깔끔하게 출력하고, 탭 두 번으로 간격 띄우기.
						System.out.printf("%d * %d = %d\t\t", (i + k), j, ((i + k) * j));
					}
				}
				System.out.println(); // 한 행(예: 1단*1, 2단*1, 3단*1) 끝나면 줄 바꾸기.
			}
			System.out.println(); // 한 그룹(예: 1~3단) 끝나면 추가로 줄 바꾸기. (그룹 간의 여백)
		}

		sc.close(); // 다 썼으면 입력 도구 깔끔하게 닫아라.
	}
}
