package dowhileex;

/* 문] 숫자 1부터 계속 더해 나가서 몇까지 더 하면 합이 100을 넘는지를 알아보는 
프로그램을 구현하시오.
*/

public class DoWhileEx06 {

	public static void main(String[] args) {

		// **sum**은 합계를 저장할 변수로, 시작은 당연히 **0**이다.
		// **i**는 1부터 계속 더해나갈 숫자를 나타내는 변수로, 초기값은 **0**이다.
		int sum = 0; 
		int i = 0;   

		// **핵심 1: `while (true)`는 무한 반복을 의미한다.**
		// 이 반복문은 조건 없이 계속 돌다가, 코드 안에서 `break`를 만나면 멈춘다.
		while (true) { 
			// **핵심 2: `if (sum > 100) break;`는 반복문을 탈출하는 조건이다.**
			// `sum`이 100을 넘는 순간(`sum > 100`이 참이 되는 순간), `break` 명령어가 실행되면서 `while` 반복문이 바로 끝난다.
			// 이 조건문이 `i++`와 `sum += i;` **위에 있다는 게 중요**하다. 이렇게 해야 "몇까지 더해야 100을 넘는가"라는 질문에 더 정확한 답을 준다.
			if (sum > 100) 
				break; 
			
			// `i++`는 `i`를 1 증가시킨다 (1, 2, 3... 순서로 더해나간다). `i`가 결국 "몇까지 더했는지"를 나타내는 숫자가 된다.
			i++;     
			// `sum += i;`는 `sum = sum + i;`와 같다. 현재 `i` 값을 `sum`에 계속 누적해서 더해나가는 핵심 연산이다.
			sum += i; 
		}
		// 반복문이 끝나고 최종 `i` 값과 `sum` 값을 출력한다.
		System.out.println("i = " + i + ", sum = " + sum);
	}
}
