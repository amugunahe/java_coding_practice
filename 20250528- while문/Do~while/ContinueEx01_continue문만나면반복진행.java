package dowhileex;

/* continue문은 반복이 진행되는 도중에 Continue문을 만나면 반복문을 끝으로 
  이동하여 다음 반복을 계속 진행함 

  문] 1과 10 사이의 숫자를 출력하되, 3의 배수인 것은 제외로 한다.
  위와 같은 결과를 출력하기 위한 프로그램을 구현하시오. 
 */

public class ContinueEX01 {

	public static void main(String[] args) {
		
		// 1부터 10까지 숫자를 반복한다.
		// 참고: 문제 요구사항은 '1과 10 사이'인데, 코드는 '0'부터 시작하네.
		// 만약 1부터 시작하려면 'i = 1'로 바꾸면 된다.
		for (int i = 0; i <= 10; i++) { 
			// 핵심: i가 3의 배수(나머지가 0)면, 이번 반복은 여기서 건너뛰고 다음 반복으로 넘어간다.
			if (i % 3 == 0) { // i를 3으로 나눈 나머지가 0이면 (즉, 3의 배수이면)
				continue; // 이 `continue`를 만나면, 아래 `System.out.print`는 실행되지 않고
			}             // for문의 다음 반복(i++)으로 바로 넘어간다.
			System.out.print(i + " "); // 3의 배수가 아닐 때만 숫자를 출력.
		}
	}
}
