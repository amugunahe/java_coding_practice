package com.inher; // 이 코드가 속한 패키지 이름입니다.

public class Point { // 'Point'라는 이름의 설계도를 만드는 부분입니다. 이 설계도를 이용해 실제 점 객체를 만들 거예요.

	int x; // 점의 x 좌표를 저장하는 변수입니다. 'int'는 정수(예: 1, 5, 100 등)를 저장할 수 있어요.
	int y; // 점의 y 좌표를 저장하는 변수입니다. 마찬가지로 정수를 저장합니다.

	// --- 생성자 (Constructor) ---
	// 생성자는 객체(점)를 만들 때 딱 한 번 실행되어, 그 객체를 초기화하는 특별한 메서드입니다.

	// 1. x와 y 좌표를 받아서 점을 만드는 생성자
	public Point(int x, int y) { // 이 생성자는 'int x'와 'int y'라는 두 개의 값을 입력받습니다.
		this.x = x; // 입력받은 'x' 값을 현재 이 Point 객체의 'x' 변수에 저장합니다.
					// 'this.x'는 "현재 객체(내가 만든 그 점)의 x"를 의미해요.
		this.y = y; // 입력받은 'y' 값을 현재 이 Point 객체의 'y' 변수에 저장합니다.
					// 'this.y'는 "현재 객체(내가 만든 그 점)의 y"를 의미해요.
	}

	// 2. 기본 생성자: x, y 좌표를 모두 0으로 초기화하는 점을 만듭니다.
	public Point() { // 이 생성자는 아무런 값도 입력받지 않습니다.
		this(0, 0); // ★ 중요: 이 코드는 바로 위에 있는 다른 생성자 `Point(int x, int y)`를 호출하는 것입니다.
					// 즉, 이 'Point()' 생성자를 호출하면 자동으로 `Point(0, 0)`이 호출되어서
					// 현재 점의 x와 y 좌표를 모두 0으로 설정해 줍니다.
					// 이렇게 하면 코드 중복을 피하고 효율적으로 초기화할 수 있어요.
	}

	// --- 메서드 (Method) ---
	// 메서드는 객체가 할 수 있는 동작이나 기능을 정의합니다.

	// 점의 x, y 좌표를 "(x,y)" 형태의 문자열로 반환하는 메서드입니다.
	String getXY() { // 'String'은 이 메서드가 문자열 형태의 결과값을 돌려준다는 의미입니다.
		return "(" + x + "," + y + ")"; // 현재 Point 객체의 x와 y 값을 이용해서 "(x,y)" 형태의 문자열을 만들어 돌려줍니다.
										// 예를 들어, x가 3이고 y가 5라면 "(3,5)"라는 문자열을 반환해요.
	}
	
	// 점의 x, y 좌표를 "x: x값, y: y값" 형태의 문자열로 반환하는 메서드입니다.
	String getLocation() { // 이 메서드도 문자열 형태의 결과값을 돌려줍니다.
		return "x: " + x + ",y:" + y; // 현재 Point 객체의 x와 y 값을 이용해서 "x:값, y:값" 형태의 문자열을 만들어 돌려줍니다.
										// 예를 들어, x가 3이고 y가 5라면 "x: 3,y:5"라는 문자열을 반환해요.
	}
}
