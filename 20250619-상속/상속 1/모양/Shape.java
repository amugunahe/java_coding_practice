package com.inher; // 이 코드가 속한 폴더(패키지) 이름이다.

/*
  Shape 클래스: 모든 '도형'들의 **존나 중요한 설계도!**
 
  핵심:
  - **모든 도형이 공통으로 가질 속성 (색깔)**이랑
  - **모든 도형이 공통으로 할 수 있는 행동 (그리기)**을 정의한다.
  - 이 클래스를 상속받는 모든 자식 도형들은 이 'color'랑 'draw()'를 **자동으로 물려받는다.**
  - 'draw()' 메소드는 여기서 대충 만들어 놓고, **자식 클래스들이 지 맘대로 다시 만든다 (오버라이딩!)**
 */

public class Shape { // 'Shape'라는 이름의 설계도를 만드는 부분이다. 이걸로 모든 도형을 만들 거다.

	// 1. 필드 (속성)
	String color = "black"; // 모든 도형이 기본적으로 가질 '색깔'. 기본값은 "black"이다.
	                        // 이 색깔은 이 새끼를 상속받는 모든 자식 도형들이 자동으로 물려받는다.

	// 2. 메소드 (기능)
	// 도형을 '그리는' 기능을 나타내는 메소드.
	// 이 메소드는 자식 클래스들이 각자의 방식으로 구체적인 '그리기' 동작을 **재정의(오버라이드)**할 거다.
	void draw() {
		// Shape의 기본 'draw()'는 그냥 도형의 색깔만 존나게 출력한다.
		System.out.printf("[color = %s]%n", color);
	}

}
