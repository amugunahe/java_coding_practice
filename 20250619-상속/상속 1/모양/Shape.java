package com.inher; // 이 코드가 속한 패키지 이름입니다.

public class Shape { // 'Shape'라는 이름의 설계도를 만드는 부분입니다. 모든 도형들이 공통적으로 가질 특성을 여기에 정의합니다.

	// 1. 필드 (속성)
	String color = "black"; // 모든 도형이 기본적으로 가질 '색상' 필드입니다.
	                        // 'String' 타입으로 문자열(예: "red", "blue")을 저장할 수 있습니다.
	                        // 여기서는 기본값을 "black"으로 설정해 놓았습니다.
	                        // 이 필드는 이 클래스를 상속받는 모든 자식 클래스들이 자동으로 물려받아 사용합니다.

	// 2. 메서드 (기능)
	// 도형을 '그리는' 기능을 나타내는 메서드입니다.
	// 'void'는 이 기능이 어떤 결과값도 돌려주지 않는다는 의미입니다.
	// 이 메서드는 자식 클래스들이 각자의 방식으로 구체적인 '그리기' 동작을 재정의(오버라이드)할 것입니다.
	void draw() {
		// Shape의 기본 `draw()` 메서드는 단순히 도형의 색상만 출력합니다.
		// `printf()`를 사용하여 형식을 지정하여 출력합니다.
		// `%s`: 문자열(String) 타입의 변수 `color`의 값을 이 위치에 출력합니다.
		// `%n`: 운영체제에 맞는 줄바꿈 문자(new line)를 출력합니다. (System.out.println()과 같은 역할)
		System.out.printf("[color = %s]%n", color);
	}

}
