package com.inher; // 이 코드는 'com.inher' 패키지에 속해 있음.

public class Point { // 'Point'라는 이름의 설계도. 점 객체를 만들 때 사용함.

	int x; // 점의 x 좌표.
	int y; // 점의 y 좌표.

	// --- 생성자 ---
	// 객체를 만들 때 초기화하는 특별한 메서드. 딱 한 번 실행됨.

	// 1. x, y 좌표를 받아서 점을 만드는 생성자
	public Point(int x, int y) { // 'x'랑 'y' 두 값을 입력받음.
		this.x = x; // 입력받은 'x'를 현재 객체의 x에 저장.
		this.y = y; // 입력받은 'y'를 현재 객체의 y에 저장.
	}

	// 2. 기본 생성자: x, y 좌표를 모두 0으로 초기화함.
	public Point() { // 아무 값도 입력받지 않음.
		this(0, 0); // ★중요: 위에 있는 'Point(int x, int y)' 생성자를 호출함.
					// 이 생성자를 호출하면 자동으로 x, y가 0으로 설정됨. (코드 중복 방지)
	}

	// --- 메서드 ---
	// 객체가 할 수 있는 동작이나 기능.

	// 점의 x, y 좌표를 "(x,y)" 형태로 반환하는 메서드.
	String getXY() {
		return "(" + x + "," + y + ")"; // "(x,y)" 형태의 문자열을 돌려줌. (예: "(3,5)")
	}
	
	// 점의 x, y 좌표를 "x: x값, y: y값" 형태로 반환하는 메서드.
	String getLocation() {
		return "x: " + x + ",y:" + y; // "x:값, y:값" 형태의 문자열을 돌려줌. (예: "x: 3,y:5")
	}
}
