// Animal.java 파일
package com.polymorphism;

/*
  Animal 클래스: 모든 동물들의 '부모' 클래스.
 
  핵심:
  - 모든 동물이 공통으로 가질 '울음소리'와 '이름'이라는 개념을 정의한다.
  - 'scream()' 메소드는 **자식 클래스에서 각 동물에 맞게 존나게 다르게 만들 거다 (오버라이딩)**.
  이게 바로 **다형성(Polymorphism)의 핵심**이다!
  - 'getName()' 메소드도 필요하면 자식 클래스에서 재정의해서 써라.
 */

public class Animal {

	// 동물의 기본 울음소리를 반환하는 메소드.
	// ★★★ 존나 중요! 자식 클래스에서 이 메소드를 재정의해서 각 동물의 고유한 울음소리를 내게 한다. ★★★
	public String scream() {
		return "동물 울음 소리"; // 모든 동물의 기본 울음소리 (재정의되기 전까지는 이거 나옴)
	}

	// 동물의 이름을 반환하는 메소드.
	// 기본값은 없지만, 필요하면 자식 클래스에서 재정의해서 구체적인 이름을 반환하게 할 수 있다.
	public String getName() {
		return null; // 기본적으로는 이름을 반환 안 함.
	}
}
