package com.inher; // 이 코드가 속한 패키지 이름입니다. 보통 관련된 클래스들을 묶어두는 폴더 같은 개념이에요.

public class Tv { // 'Tv'라는 이름의 설계도를 만드는 부분입니다. 이 설계도를 이용해서 실제 TV 객체를 만들 거예요.

	boolean power; // TV의 전원 상태를 저장하는 변수입니다. 'boolean'은 참(true) 또는 거짓(false)만 저장할 수 있어요.
					// true는 TV가 켜져 있다는 뜻이고, false는 TV가 꺼져 있다는 뜻입니다.
	int channel; 	// TV의 현재 채널 번호를 저장하는 변수입니다. 'int'는 정수(예: 1, 50, 100 등)를 저장할 수 있어요.

	// --- TV 기능들 ---

	// TV 전원을 켜고 끄는 동작을 수행하는 메서드(기능)입니다.
	void power() { // 'void'는 이 기능이 어떤 결과값도 돌려주지 않는다는 의미입니다.
		power = !power; // 이 부분이 핵심이에요!
						// 현재 'power' 변수에 저장된 값을 반대로 바꿔줍니다.
						// 만약 power가 true(켜짐)였다면 false(꺼짐)로,
						// false(꺼짐)였다면 true(켜짐)로 바뀝니다.
						// '!'는 '아니다'라는 의미로, 논리 값을 반전시킬 때 사용해요.
	}

	// TV 채널을 1씩 올리는 동작을 수행하는 메서드(기능)입니다.
	void channelUp() {
		++channel; // 'channel' 변수의 값을 1 증가시킵니다.
					// 예를 들어, 현재 채널이 5번이었다면 이 기능을 실행하면 6번이 됩니다.
					// '++'는 변수의 값을 1 증가시키는 연산자입니다.
	}

	// TV 채널을 1씩 내리는 동작을 수행하는 메서드(기능)입니다.
	void channelDown() {
		--channel; // 'channel' 변수의 값을 1 감소시킵니다.
					// 예를 들어, 현재 채널이 10번이었다면 이 기능을 실행하면 9번이 됩니다.
					// '--'는 변수의 값을 1 감소시키는 연산자입니다.
	}

}
