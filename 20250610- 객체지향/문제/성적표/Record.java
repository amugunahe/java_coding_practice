package com.sungex; // 'com.sungex'는 이 자바 파일(클래스)이 속한 패키지 이름입니다.
                    // 패키지는 관련된 클래스들을 묶어서 관리하는 폴더 같은 역할을 해요.

/*
  이 'Record' 클래스는 '하나의 성적 기록'을 나타내는 설계도입니다.
  주로 학생의 이름, 과목별 점수, 그리고 그로부터 계산되는 총점, 평균, 석차와 같은
  '속성(데이터)'만을 저장하는 데 사용됩니다.
  다른 클래스(예: 성적 관리 프로그램)에서 이 'Record' 객체를 생성하여
  각 학생의 성적 데이터를 담는 데 활용할 수 있습니다.
 */

public class Record { // 'Record'라는 이름의 공개(public) 클래스를 선언합니다.
                     // 이 클래스는 '성적 기록' 객체를 만들기 위한 '설계도' 또는 '틀'입니다.

    // --- 멤버 변수 (필드) ---
    // 이들은 'Record' 클래스로 만들어지는 각각의 객체가 가질 고유한 정보들을 저장하는 공간입니다.
    // 현재 이 변수들은 'private' 키워드 없이 선언되어 있습니다.
    // 'private'이 없으면 같은 패키지 내의 다른 클래스에서 이 변수들에 직접 접근할 수 있습니다.
    // (보통은 데이터 보호를 위해 'private'으로 선언하고 'getter/setter' 메소드를 사용하는 것이 권장됩니다.)

	String name; // 학생의 '이름'을 저장할 문자열(String) 변수입니다.

	// 'score'는 국어, 영어, 수학 세 과목의 점수를 저장할 정수(int) 배열입니다.
    // 'new int[3]'은 3개의 정수(score[0], score[1], score[2])를 저장할 수 있는 공간을 메모리에 할당한다는 의미입니다.
    // 예를 들어, score[0]은 국어 점수, score[1]은 영어 점수, score[2]는 수학 점수를 저장하는 용도로 사용할 수 있습니다.
	int[] score = new int[3];

	int sum; // 국어, 영어, 수학 점수의 '총점'을 저장할 정수(int) 변수입니다.

	double avg; // 세 과목 점수의 '평균'을 저장할 실수(double) 변수입니다.
                // 평균은 소수점 이하가 나올 수 있으므로 'double' 타입으로 선언하는 것이 적절합니다.

	int rank; // 학생의 '석차' (등수)를 저장할 정수(int) 변수입니다.
} // Record 클래스 종료
