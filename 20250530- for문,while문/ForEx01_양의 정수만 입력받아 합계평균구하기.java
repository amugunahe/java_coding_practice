package com.exam; // 이 코드가 속한 패키지(폴더) 이름이다.

/*
  ForEx01 클래스: **양의 정수만 입력받아서 합계랑 평균을 존나게 계산하는 프로그램이다!**

  핵심:
  - **문제:** 사용자한테 양의 정수를 계속 입력받는다. 음수가 입력되면 그 시점에서 입력을 멈추고,
  지금까지 입력된 양의 정수들의 합계와 평균을 출력한다!
  - **`Scanner` 클래스:** 사용자한테 키보드로 입력받을 때 쓰는 존나 유용한 놈이다!
  - **`while` 루프:** 음수가 입력되기 전까지 **무한히 반복해서 숫자들을 입력받는다!** (이게 핵심이다!)
  - **`n >= 0` 조건:** `while` 루프의 조건이다. 'n'이 0보다 크거나 같을 동안만 반복한다.
  - **`sum += n;`:** 입력받은 'n' 값을 'sum' 변수에 계속 더해서 총 합계를 만든다!
  - **`cnt++;`:** 몇 개의 숫자를 입력받았는지 세는 카운터다! (평균 낼 때 존나 중요!)
  - **평균 계산 주의:** 정수끼리 나누면 소수점 버려진다, 씨발! (예: 5 / 2 = 2)
  소수점까지 정확한 평균을 원하면 형 변환을 해줘야 한다!
 */

import java.util.*; // 사용자로부터 키보드 입력을 받기 위해 'Scanner' 클래스가 포함된 java.util 패키지 전체를 불러온다.

public class ForEx01 {

	public static void main(String[] args) {

		// ★★★ 사용자 입력을 받을 'Scanner' 객체 생성! ★★★
		Scanner sc = new Scanner(System.in);

		// ★★★ 변수 선언 및 초기화! ★★★
		// `n`: 사용자로부터 입력받을 정수.
		// `sum`: 입력받은 정수들의 합계.
		// `cnt`: 입력받은 정수들의 개수.
		// 모두 0으로 초기화한다. (존나 중요!)
		int n;
		int sum;
		int cnt;
		n = sum = cnt = 0; // 이 한 줄로 세 변수를 동시에 0으로 초기화!

		/*
		 * --- 니가 주석 처리한 `for` 루프 예시 ---
		 * (이 루프는 `while` 루프랑 같은 기능을 할 수 있다. 방식만 좀 다르다!)
		 * for (; n >= 0;) {
		 * sum += n;
		 * System.out.print("정수 입력:");
		 * n = sc.nextInt();
		 * cnt++;
		 * }
		 */
		
		// ★★★ 메인 루프: 음수가 입력될 때까지 존나게 반복해서 입력받고 계산한다! ★★★
		while (n >= 0){ // 'n'이 0보다 크거나 같을 동안 (즉, 양수나 0일 동안) 반복한다.
			sum += n; // ★★★ 중요! 현재 'n' 값을 'sum'에 더한다. ★★★
			          // (루프 시작 시 'n'은 0이므로 첫 번째 합계에 영향을 주지 않는다.)
			System.out.print("정수 입력: "); // 사용자에게 정수를 입력하라는 메시지 출력.
			n = sc.nextInt(); // 사용자가 입력한 정수를 'n' 변수에 저장한다.
			cnt++; // 입력받은 숫자의 개수를 1 증가시킨다.
		}
		
		// ★★★ 최종 합계 출력! ★★★
		System.out.println("합계: " + sum);
		
		// ★★★ 최종 평균 출력! (존나 중요! 소수점 나오게 형 변환!) ★★★
		// 'sum'을 `float`으로 강제 형 변환(`(float)sum`)해서 나누면 소수점까지 평균이 나온다!
		// `cnt - 1`을 하는 이유: 마지막에 음수가 입력되었을 때도 `cnt`가 증가했기 때문에,
		// 실제 양의 정수 개수보다 1이 더 많다. 그래서 하나 빼줘야 정확한 평균이 나온다, 씨발!
		// (단, `cnt - 1`이 0이 되는 경우(첫 입력부터 음수)는 나눗셈 오류가 생길 수 있으니 실제로는 예외 처리 필요!)
		System.out.println("평균: " + (float)sum / (cnt - 1));

		// ★★★ Scanner 닫기! (존나 중요!) ★★★
		// 자원 낭비를 막기 위해 다 쓴 'Scanner'는 꼭 닫아줘야 한다!
		sc.close();
	}
}
