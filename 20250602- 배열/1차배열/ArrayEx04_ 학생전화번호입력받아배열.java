package com.arrayex; // 이 코드가 속한 폴더(패키지) 이름이다.

/*
  ArrayEx04 클래스: **입력받은 인원수만큼 학생 이름이랑 전화번호를 존나게 저장하고 출력하는 예제!**
 
  핵심:
  - **문제:** 사용자한테 몇 명의 학생 정보를 입력받을지 물어보고, 그 인원수만큼 학생 이름과 전화번호를 입력받아서 전부 출력한다!
  - **배열 활용:** 이름은 `String` 배열에, 전화번호도 `String` 배열에 따로따로 저장한다!
  - **`Scanner` 클래스:** 사용자한테 키보드로 입력받을 때 쓰는 존나 유용한 놈이다!
  - **`nextInt()`:** 숫자를 입력받을 때 쓴다.
  - **`next()`:** 공백 앞까지의 문자열(단어 하나)을 입력받을 때 쓴다. (이름, 전화번호처럼 공백으로 구분된 단어에 딱이다!)
  - **`for` 문:** 입력받은 인원수만큼 반복해서 정보를 받고, 저장된 정보를 출력할 때 존나게 유용하다!
 */

import java.util.*; // 'Scanner' 클래스를 쓰려면 이 새끼를 불러와야 한다, 씨발!

public class ArrayEx04 {

	public static void main(String[] args) {

		int n; // 입력받을 학생 수를 저장할 변수.

		// ★★★ 사용자 입력받을 준비! ★★★
		// 'Scanner' 객체를 만들어서 시스템 입력(키보드)을 읽어들인다.
		Scanner sc = new Scanner(System.in);

		// --- 입력 처리할 학생 수 받기 ---
		System.out.print("입력 처리할 학생 수:"); // 메시지 출력.
		n = sc.nextInt(); // 사용자가 입력한 숫자를 'n'에 저장한다. (예: 3)

		// ★★★ 학생 이름 저장할 배열 선언! ★★★
		// 입력받은 학생 수 'n'만큼의 크기를 가진 String 배열 'name'을 만든다.
		String[] name = new String[n];

		// ★★★ 전화번호 저장할 배열 선언! ★★★
		// 똑같이 'n'만큼의 크기를 가진 String 배열 'phone'을 만든다.
		String[] phone = new String[n];

		// --- 학생 이름과 전화번호 입력받기! ---
		// 'n'번 반복하면서 학생 한 명씩 이름과 전화번호를 입력받아 배열에 저장한다.
		for (int i = 0; i < n; i++) {
			System.out.print("이름 전화번호 입력(" + (i + 1) + ")[공백구분]:"); // 몇 번째 학생인지 표시.
			name[i] = sc.next();  // ★★★ 존나 중요! 'sc.next()'는 공백 앞까지의 문자열을 읽는다! ★★★
			phone[i] = sc.next(); // 이름 다음에 오는 전화번호를 읽는다.
		}
		
		// --- 전체 학생 수 출력! ---
		System.out.println("전체 학생 수: " + n + "명");

		// --- 헤더 출력 (이름, 전화번호) ---
		System.out.println("이름\t전화번호"); // '이름'과 '전화번호'를 탭으로 띄워서 출력.

		// --- 입력받은 내용 전체 출력! ---
		// 'n'번 반복하면서 배열에 저장된 모든 학생의 이름과 전화번호를 출력한다.
		for (int i = 0; i < n; i++) {
			System.out.println(name[i] + " " + phone[i]); // 이름과 전화번호를 공백으로 구분해서 한 줄씩 출력!
		}
		
		// ★★★ 스캐너 닫기! (존나 중요!) ★★★
		// 자원 낭비를 막기 위해 다 쓴 'Scanner'는 꼭 닫아줘야 한다!
		sc.close();
	}
}
