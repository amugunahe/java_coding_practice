package com.arrayex; // 이 코드가 속한 폴더(패키지) 이름이다.

/*
  ArrayEx02 클래스: **배열 안의 숫자들 중에 짝수랑 3의 배수를 존나게 찾아내는 예제!**
 
  핵심:
  - **문제:** 임의의 숫자들로 채워진 배열에서 짝수만 뽑아내고, 3의 배수만 뽑아내서 출력한다!
  - **풀이:**
  1. 배열의 모든 요소를 순서대로 돌면서 하나씩 확인한다. (for 문 쓴다!)
  2. **짝수 찾기:** 숫자를 2로 나눴을 때 나머지가 0이면 짝수다! (`숫자 % 2 == 0`)
  3. **3의 배수 찾기:** 숫자를 3으로 나눴을 때 나머지가 0이면 3의 배수다! (`숫자 % 3 == 0`)
  - **`System.out.print()` vs `System.out.println()`:**
  - `print()`: 출력하고 줄 안 바꾼다!
  - `println()`: 출력하고 줄 바꾼다! (`\n`도 줄 바꿈!)
 */

public class ArrayEx02 {

	public static void main(String[] args) {

		// ★★★ 임의의 숫자들로 채워진 배열 선언 및 초기화! ★★★
		// 여기에 니가 찾고 싶은 숫자들을 넣어라, 씨발!
		int arr[] = { 4, 7, 9, 1, 3, 2, 5, 6, 8 };

		// --- 배열 전체 요소 출력! ---
		System.out.println("배열 요소 전체 출력:");
		
		// for 문으로 배열의 첫 번째 칸(인덱스 0)부터 마지막 칸까지 다 돌면서 출력한다!
		for (int i = 0; i < arr.length; i++) {
			System.out.print(arr[i] + "\t"); // 각 요소를 출력하고 탭(`\t`)으로 간격 띄운다!
		}
		System.out.println(); // 한 줄 출력했으니 다음 출력은 새 줄에서 시작하게 줄 바꿈!

		// --- 짝수만 골라서 출력! ---
		System.out.println("\n짝수 출력:"); // 줄 바꿈(`\n`)하고 "짝수 출력:" 메시지 출력.
		// 다시 for 문으로 배열을 처음부터 끝까지 돈다!
		for (int i = 0; i < arr.length; i++) {
			if (arr[i] % 2 == 0) { // ★★★ 존나 중요! 2로 나눴을 때 나머지가 0이면 짝수다! ★★★
				System.out.print(arr[i] + "\t"); // 짝수만 출력하고 탭으로 간격 띄운다!
			}
		}
		System.out.println(); // 한 줄 출력했으니 다음 출력은 새 줄에서 시작하게 줄 바꿈!

		// --- 3의 배수만 골라서 출력! ---
		System.out.println("\n3의 배수 출력:"); // 줄 바꿈(`\n`)하고 "3의 배수 출력:" 메시지 출력.
		// 또 다시 for 문으로 배열을 처음부터 끝까지 돈다!
		for (int i = 0; i < arr.length; i++) {
			if (arr[i] % 3 == 0) { // ★★★ 존나 중요! 3으로 나눴을 때 나머지가 0이면 3의 배수다! ★★★
				System.out.print(arr[i] + "\t"); // 3의 배수만 출력하고 탭으로 간격 띄운다!
			}
		}
		System.out.println(); // 모든 출력이 끝난 후 깔끔하게 줄 바꿈!
	}
}
