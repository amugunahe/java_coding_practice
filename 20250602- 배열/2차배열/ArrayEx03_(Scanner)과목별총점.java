package com.arrayex; // 이 코드가 속한 폴더(패키지) 이름이다.

/*
  ArrayEx03 클래스: **2차원 배열을 활용해서 학생들 성적을 존나게 깔끔하게 출력하는 예제!**
 
  핵심:
  - **문제:** 학생 5명의 국어, 영어, 수학 점수가 2차원 배열에 저장되어 있다.
  각 학생별 총점과 평균을 계산하고, 과목별 총점도 계산해서 예시처럼 표로 출력한다!
  - **2차원 배열 `score`:** `score[학생번호][과목번호]` 형태로 점수를 저장한다.
  (예: `score[0][0]`는 첫 번째 학생의 국어 점수!)
  - **중첩 `for` 문:**
  - 바깥쪽 `for` 문: 각 학생(행)을 돌면서 학생별 계산 및 출력을 한다!
  - 안쪽 `for` 문: 각 학생의 과목(열)을 돌면서 점수를 합산하고 출력한다!
  - **`printf()` 메소드:** "존나 깔끔하게" 형식을 맞춰서 출력할 때 쓰는 메소드다! (정렬, 소수점 자리수 맞출 때 딱이다!)
  - `%5d`: 정수를 5칸에 맞춰서 출력 (오른쪽 정렬)
  - `%6.1f`: 실수를 총 6칸에 맞춰서 소수점 첫째 자리까지 출력
  - `%n`: 줄 바꿈 (운영체제 상관없이 잘 작동한다!)
 */

public class ArrayEx03 {

	public static void main(String[] args) {

		// ★★★ 학생들 점수가 들어있는 2차원 배열 선언 및 초기화! ★★★
		// `score[5][3]` 배열이다. (5명의 학생, 각 3과목 점수)
		int score[][] = {
				{ 100, 100, 100 }, // 1번 학생 (국어, 영어, 수학)
				{ 20, 20, 20 },   // 2번 학생
				{ 30, 30, 30 },   // 3번 학생
				{ 40, 40, 40 },   // 4번 학생
				{ 50, 50, 50 },   // 5번 학생
		};

		// ★★★ 과목별 총점 변수 선언 및 초기화! ★★★
		// 모든 학생의 국어, 영어, 수학 점수를 각각 더할 변수다.
		int korTot = 0, engTot = 0, mathTot = 0;

		// ★★★ 각 학생 개인별 총점과 평균 변수 선언! ★★★
		// `sum`은 각 학생의 총점을 임시로 저장하고, `avg`는 평균을 저장한다.
		int sum = 0;
		float avg = 0.0f; // 평균은 소수점이 나올 수 있으니 `float`으로 선언한다!

		// --- 출력 헤더 (표의 맨 위 제목 부분) ---
		System.out.println("-------------------------------------------");
		System.out.println("번호\t\t국어\t\t영어\t\t수학\t\t총점\t\t평균"); // 탭(`\t`)으로 간격 맞춘다!
		System.out.println("-------------------------------------------");

		// ★★★ 각 학생별 총점 및 평균 계산, 그리고 출력! (바깥 for 문: 학생(행) 순회) ★★★
		for (int i = 0; i < score.length; i++) { // `i`는 학생 인덱스 (0부터 4까지, 총 5명!)

			// --- 과목별 총점에 현재 학생의 점수 누적! ---
			korTot += score[i][0];  // 현재 학생의 국어 점수를 `korTot`에 더한다.
			engTot += score[i][1];  // 현재 학생의 영어 점수를 `engTot`에 더한다.
			mathTot += score[i][2]; // 현재 학생의 수학 점수를 `mathTot`에 더한다.

			sum = 0; // ★★★ 존나 중요! 다음 학생으로 넘어가기 전에 `sum`을 0으로 초기화한다! ★★★
					 // 안 그러면 이전 학생의 점수가 계속 쌓여버린다, 씨발!
		
			// --- 학생 번호 출력! ---
			System.out.print(i + 1 + "\t\t"); // 1번부터 시작하도록 `i + 1`을 출력. 탭으로 간격 맞춘다.
			
			// ★★★ 각 학생의 과목 점수 합산 및 출력! (안쪽 for 문: 과목(열) 순회) ★★★
			for (int j = 0; j < score[i].length; j++) { // `j`는 과목 인덱스 (0부터 2까지, 총 3과목!)
				sum += score[i][j]; // 현재 학생의 각 과목 점수를 `sum`에 더해서 총점을 만든다.
				
				System.out.printf("%5d\t\t", score[i][j]); // 각 과목 점수를 5칸에 맞춰 출력하고 탭으로 간격 띄운다!
			}
			
			// --- 각 학생별 평균 계산! ---
			// `score[i].length`는 현재 학생이 가진 과목 수다 (여기선 3)!
			// `(float)score[i].length`는 나눗셈 결과가 소수점으로 나오도록 형 변환하는 거다! (존나 중요!)
			avg = sum / (float) score[i].length;

			// --- 각 학생별 총점과 평균 출력! ---
			// `sum`은 5칸, `avg`는 소수점 첫째 자리까지 6칸에 맞춰 출력하고 줄 바꿈(`%n`)!
			System.out.printf("%5d\t\t%6.1f%n", sum, avg);
		}

		// --- 표의 마지막 선 출력! ---
		System.out.println("-------------------------------------------");

		// --- 과목별 최종 총점 출력! ---
		// `korTot`, `engTot`, `mathTot` 변수에 누적된 각 과목의 총점을 4칸에 맞춰 출력하고 줄 바꿈!
		System.out.printf("총점:\t\t%4d\t\t%4d\t\t%4d%n", korTot, engTot, mathTot);
		System.out.println("-------------------------------------------"); // 마무리 선
	}
}
